function timeCalc(myArray) {
  var minutes = 0;  //declaring my variables
  var seconds = 0;  //declaring my variables
  var minPattern = /(\d{1,2}):/;  //pattern for minutes
  var secPattern = /:(\d{1,2})/;  //pattern for seconds
  myArray.forEach((element)=>{     //loop through each elements to add minutes and seconds
    minutes += parseInt(element.match(minPattern)[1]);       //convert to numbers to do calculatiions
    seconds += parseInt(element.match(secPattern)[1]);      //convert to numbers to do calculations and sum up
  });
  
  var secs2min = (seconds/60);    //convert my seconds to minutes
  var totaltime_min= minutes+secs2min;  //total up my minutes
  var totaltime_hour = totaltime_min/60;   //roughtly how many hours
  
  return totaltime_hour;
}

